//定时器输入捕获中断处理回调函数
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) //捕获中断发生时执行
{
  if (TIM2 == htim->Instance)
  {
    Capture_Buf[Capture_Cnt] = (HAL_TIM_ReadCapturedValue(&htim2, TIM_CHANNEL_3) + 65536 * Capture_Flow); //获取当前计数器的值
    if (Capture_Cnt >= 1 && (Capture_Buf[Capture_Cnt] < Capture_Buf[Capture_Cnt - 1]))
    {
      Capture_Buf[Capture_Cnt] += 65536;
      Capture_Flow++;
    }

    if (Capture_Cnt % 2 == 1)
    {
      __HAL_TIM_SET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3, TIM_ICPOLARITY_FALLING); //配置TIM2通道3下降沿触发捕获
    }
    if (Capture_Cnt % 2 == 0) //当前进入中断是下降沿触发
    {
      __HAL_TIM_SET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3, TIM_ICPOLARITY_RISING); //定时器2通道3设置为上升沿触发捕获
    }
    Capture_Cnt++;
    if (Capture_Cnt == 129)
    {
      __HAL_TIM_SET_CAPTUREPOLARITY(&htim2, TIM_CHANNEL_3, TIM_ICPOLARITY_FALLING); //配置TIM2通道3下降沿触发捕获
      HAL_TIM_IC_Stop_IT(&htim2, TIM_CHANNEL_3);                                    //关闭输入捕获
      Capture_Flag = 1;
      Capture_Cnt = 0;
      Capture_Flow = 0;
    }
  }
}

